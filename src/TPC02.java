public class TPC02 {
    public static void main(String[] args) {
        //프로그래밍의 3대 요소 : 변수, 자료형(Data Type) , 할당 ( = )

        int a,b,c;
        a = 1;
        b = 1;
        c = a+b;
        System.out.println(c);


        float f ; //실수형 데이터는 float와 double이 있는데 자바는 기본적으로 double로 생각. float로 하려면 뒤에 표시
        f = 34.5f;
        System.out.println(f);

        char e ;
        e = 'E';
        System.out.println(e);

        boolean g;
        g = true; //true 는 참 false는 거짓
        System.out.println(g);

    }

}
